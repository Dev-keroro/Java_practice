package org.jDev_keroro_java_practic_numberstring;

public class Nuber {

	public static void main(String[] args) {
		System.out.println(1+2);
		System.out.println("생활코딩");
		System.out.println("생활코딩2");
		System.out.println("생");	//큰따옴표로 묶여 있기 때문에 문자가 아니라 문자열이된다.
		System.out.println("생활코딩"+"입니다"); // 문자열 더하기 문자열	
		System.out.println("생활코딩 \"큰 따옴표를 표시하고 싶다!\""); // \기호를 이용하여 큰따옴표를 만들수 있다.
	}

}
