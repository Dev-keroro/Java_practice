package org.Dev_keroro.java.array;

public class LengthDemo {

	public static void main(String[] args) {
		String[] classGroup = new String[4];
		
		classGroup[0] = "최진혁";
		System.out.println(classGroup.length);
		classGroup[1] = "최유빈";
		System.out.println(classGroup.length);
		classGroup[2] = "한이람";
		System.out.println(classGroup.length);
		classGroup[3] = "케로로";
		System.out.println(classGroup.length);
		

	}

}
